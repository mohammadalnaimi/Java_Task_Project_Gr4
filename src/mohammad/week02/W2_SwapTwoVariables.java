package mohammad.week02;

public class W2_SwapTwoVariables {
    public static void main(String[] args) {
        int a = 20;
        int b = 10;

        System.out.println(a);
        System.out.println(b);
        a=a+b;
        b=a-b;
        a=a-b;

        System.out.println(a);
        System.out.println(b);
    }
}
